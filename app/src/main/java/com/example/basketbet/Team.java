package com.example.basketbet;

public final class Team {

    private String name;

    private int num;

    public Team() {

    }

    public void setNum(int pos) {
        num = pos;
    }

    public int getNum() {
        return num;
    }

    public void setName(String setName) {
        name = setName;
    }

    public String getName() {
        return name;
    }

}
